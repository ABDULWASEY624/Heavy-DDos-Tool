char *generate_random_ua(void) {
    int idx = rand() % NUM_USER_AGENTS;
    return strdup(user_agents[idx]);
}

char *generate_random_ip(void) {
    char *ip = malloc(16);
    snprintf(ip, 16, "%d.%d.%d.%d",
             rand() % 255 + 1, rand() % 256, rand() % 256, rand() % 256);
    return ip;
}
